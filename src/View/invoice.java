    private void btnthanhtoanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnthanhtoanActionPerformed
        // TODO add your handling code here:
        int chon, index;
        String ma;
        index = tbldata.getSelectedRow();
        chon = JOptionPane.showConfirmDialog(this, "Bạn Có Muốn Xoá Không", "Xoá Dữ liệu", JOptionPane.YES_NO_CANCEL_OPTION, 3, null);
        if (chon == 0) {
            ma = tbldata.getValueAt(index, 2).toString();
            ivs.delinvoice(ma);
            JOptionPane.showMessageDialog(this, "Xoá Thành Công");
            this.filltable(ivs.getall());

        } else {
            JOptionPane.showMessageDialog(this, "Xoá Thất Bại");
        }
    }//GEN-LAST:event_btnthanhtoanActionPerformed

    private void btnchangeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnchangeActionPerformed
        // TODO add your handling code here:
        if (this.invoicedata()) {
            index = tbldata.getSelectedRow();
            Invoice iv = this.readform();
            if (index == -1) {
                JOptionPane.showMessageDialog(this, "Bạn Chưa chọn sửa");
            } else {
                String ma = tbldata.getValueAt(index, 1).toString();
                if (ivs.updateinvoice(ma, iv) > 0) {
                    JOptionPane.showMessageDialog(this, "Sửa Thành Công");
                    this.filltable(ivs.getall());
                } else {
                    JOptionPane.showMessageDialog(this, "Sửa Thất Bại");
                }
            }
        }
    }//GEN-LAST:event_btnchangeActionPerformed

    private void btnfindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnfindActionPerformed
        // TODO add your handling code here:
        
        // TODO add your handling code here:
        String ma = JOptionPane.showInputDialog("Nhập mã hoá đơn");
        if (ivs.findinvoice(ma).isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không Tìm Thấy");
            return;
        }
        filltable(ivs.findinvoice(ma));
    }//GEN-LAST:event_btnfindActionPerformed

