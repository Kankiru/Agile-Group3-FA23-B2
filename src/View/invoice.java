    public void filltable(List<Invoice> list) {
        model = (DefaultTableModel) tbldata.getModel();
        model.setRowCount(0);
        for (Invoice x : list) {
            model.addRow(x.todatarow());
        }
    }

    public void showdetail(int index) {
        txttenkh.setText(tbldata.getValueAt(index, 0).toString());
        txttennv.setText(tbldata.getValueAt(index, 1).toString());
        txtmahoadon.setText(tbldata.getValueAt(index, 2).toString());
        txtloaihoa.setText(tbldata.getValueAt(index, 3).toString());
        txtsoluong.setText(tbldata.getValueAt(index, 4).toString());
        txttongtien.setText(tbldata.getValueAt(index, 5).toString());
    }

    Invoice readform() {
        String tenkh, tennv, ma, loaihoa;
        int soluong, tongtien;
        tenkh = txttenkh.getText().trim();
        tennv = txttennv.getText().trim();
        ma = txtmahoadon.getText().trim();
        loaihoa = txtloaihoa.getText().trim();
        soluong = Integer.parseInt(txtsoluong.getText().trim());
        tongtien = Integer.parseInt(txttongtien.getText().trim());
        return new Invoice(tenkh, tennv, ma, loaihoa, soluong, tongtien);
    }

    boolean invoicedata() {
        if (txttenkh.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống tên khách");
        }
        if (txttennv.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống tên nhân viên");
        }
        if (txtmahoadon.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống mã hoá đơn");
        }
        if (txtloaihoa.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống loại hoa");
        }
        if (txtsoluong.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống số lượng");
        }
        if (txttongtien.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống tổng tiền");
        }
        return true;
    }

