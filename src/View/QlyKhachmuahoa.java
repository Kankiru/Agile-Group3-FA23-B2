    public void filltable(List<Customer> List) {
        model = (DefaultTableModel) tbldata.getModel();
        model.setRowCount(0);
        for (Customer x : List) {
            model.addRow(x.todatarow());
        }
    }

    public void showdetail(int index) {
        txtmakh.setText(csv.getall().get(index).getMa());
        txthoten.setText(csv.getall().get(index).getHoten());
        txtemail.setText(csv.getall().get(index).getEmail());
        txtsdt.setText(csv.getall().get(index).getSdt());
        txtdiachi.setText(csv.getall().get(index).getDiachi());
        if (csv.getall().get(index).getGioitinh() == 0) {
            rdonam.setSelected(true);
        } else {
            rdonu.setSelected(true);
        }
    }

    Customer readform() {
        String ma, hoten, email, sdt, diachi;
        int gt;
        ma = txtmakh.getText().trim();
        hoten = txthoten.getText().trim();
        email = txtemail.getText().trim();
        sdt = txtsdt.getText().trim();
        diachi = txtdiachi.getText().trim();
        if (rdonam.isSelected()) {
            gt = 0;
        } else {
            gt = 1;
        }
        return new Customer(ma, hoten, email, sdt, diachi, gt);
    }

    boolean Cusdata() {
        if (txtmakh.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống mã");
        }
        if (txthoten.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống Tên");
        }
        if (txtemail.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống email");
        }
        if (txtsdt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống sdt");
        }
        if (txtdiachi.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống địa chỉ");
        } else {
            try {
                if (txtsdt.getText() == "" || txtsdt.getText().length() != 12) {
                    JOptionPane.showMessageDialog(this, "Số Điện Thoại Phải là 12 số");
                    return false;
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Số điện Thoại Phải là số");
            }
        }
        return true;
    }

