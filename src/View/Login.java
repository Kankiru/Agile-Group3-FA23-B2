    boolean checkuser() {
        if (txtusername.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Tài khoản Đang Trống");
            return false;
        }
        if (txtpassword.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Mật khẩu Đang Trống");
            return false;
        }
        return true;
    }

